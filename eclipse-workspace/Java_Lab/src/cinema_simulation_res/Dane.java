package cinema_simulation_res;
public class Dane {
	public String imie = "";
	public String nazwisko = "";
	public Dane(){
		}
	public Dane(String imie, String nazwisko){
		this.imie = imie;
		this.nazwisko = nazwisko;
	}
	
	public String getImie() {
		return imie;
	}
	public String getNazwisko() {
		return nazwisko;
	}
}
